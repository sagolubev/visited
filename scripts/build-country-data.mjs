import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const sourcePath = path.join(root, "countries.txt");
const outputPath = path.join(root, "js", "countries.generated.js");

function parseNumber(value, label, lineNumber) {
	const parsed = Number(value);
	if (!Number.isFinite(parsed)) {
		throw new Error(`Invalid ${label} on line ${lineNumber}: ${value}`);
	}
	return parsed;
}

function cleanPart(value) {
	return value.trim().replace(/\s+/g, " ");
}

const source = await readFile(sourcePath, "utf8");
const countries = [];
const cities = [];
const nodes = [];

for (const [index, rawLine] of source.split(/\r?\n/).entries()) {
	const lineNumber = index + 1;
	const line = rawLine.trim();
	if (!line || line.startsWith("#")) continue;

	const parts = rawLine.split("|").map(cleanPart);
	if (parts.length !== 5) {
		throw new Error(
			`Invalid format on line ${lineNumber}. Expected 5 pipe-separated fields.`,
		);
	}

	const [type, first, second, latRaw, lngRaw] = parts;
	const lat = parseNumber(latRaw, "latitude", lineNumber);
	const lng = parseNumber(lngRaw, "longitude", lineNumber);

	if (type === "country") {
		const entry = { country: first, name: second, lat, lng };
		countries.push(first);
		nodes.push(entry);
	} else if (type === "city") {
		const entry = { name: first, country: second, lat, lng };
		cities.push(first);
		nodes.push(entry);
	} else {
		throw new Error(
			`Invalid type on line ${lineNumber}: ${type}. Use "country" or "city".`,
		);
	}
}

if (countries.length === 0)
	throw new Error("countries.txt must contain at least one country");
if (nodes.length < 2)
	throw new Error("countries.txt must contain at least two nodes");

const payload = {
	sourceHash: createHash("sha256").update(source).digest("hex"),
	countries,
	cities,
	nodes,
};

const js = `// Generated by scripts/build-country-data.mjs from countries.txt. Do not edit manually.\nwindow.N1O_COUNTRY_DATA = ${JSON.stringify(payload, null, 2)};\n`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, js, "utf8");
console.log(
	`Generated ${path.relative(root, outputPath)} from ${path.relative(root, sourcePath)} (${countries.length} countries, ${cities.length} cities, ${nodes.length} nodes)`,
);
