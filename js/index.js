	function camera(alpha, beta) {
		const sinA = Math.sin(alpha);
		const cosA = Math.cos(alpha);
		const sinB = Math.sin(beta);
		const cosB = Math.cos(beta);
		const cx = 2 * cosA * cosB;
		const cy = 2 * sinA * cosB;
		const cz = 2 * sinB;
		return {
			m: [
				-sinA,
				cosA,
				0,
				0,
				cosA * sinB,
				sinA * sinB,
				-cosB,
				0,
				cosA * cosB,
				sinA * cosB,
				sinB,
				0,
				cx,
				cy,
				cz,
				1,
			],
			cx,
			cy,
			cz,
		};
	}

	function currentAngle(now) {
		if (prefersReducedMotion) return state.angleOffset;
		if (state.dragging) {
			return (
				(((state.grab.timeAtGrab - state.startedAt) % ROTATION_MS) /
					ROTATION_MS) *
					TAU +
				state.angleOffset
			);
		}
		return (
			(((now - state.startedAt) % ROTATION_MS) / ROTATION_MS) * TAU +
			state.angleOffset
		);
	}

